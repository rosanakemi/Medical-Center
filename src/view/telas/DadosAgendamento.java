    private void botao_ag_agendarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_ag_agendarActionPerformed

        if (this.paciente == null) {
            JOptionPane.showMessageDialog(null, "Consulta um Paciente para agendar.");
            campo_pac_cpf.requestFocus();
            return;
        }

        Medico medicoSelecionado = (Medico) combo_medico.getSelectedItem();
        if (medicoSelecionado == null) {
            JOptionPane.showMessageDialog(null, "Consulta um Medico para agendar.");
            return;
        }

        String dateString = campo_ag_data.getText();
        String timeString = campo_ag_horario.getText();

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm");

        try {
            Date date = dateFormat.parse(dateString);
            Date time = timeFormat.parse(timeString);

            // Combinar data e hora
            Calendar dateTime = Calendar.getInstance();
            Calendar currentDate = Calendar.getInstance();

            dateTime.setTime(date);
            dateTime.set(Calendar.HOUR_OF_DAY, time.getHours());
            dateTime.set(Calendar.MINUTE, time.getMinutes());
            dateTime.set(Calendar.SECOND, time.getSeconds());

            // Comparar com a data e hora atuais
            if (dateTime.before(currentDate)) {
                JOptionPane.showMessageDialog(this, "Não é possível agendar em uma data anterior.", "Erro", JOptionPane.ERROR_MESSAGE);
            } else {
                AgendamentoServico servico = new AgendamentoServico();
                if (this.agendamento == null) {
                    if (servico.agendar(medicoSelecionado, paciente, dateTime)) {
                        JOptionPane.showMessageDialog(null, "Agendamento realizado com sucesso.");
                        this.dispose();
                        Home view = new Home();
                        view.setVisible(true);
                    }
                } else {
                    if (servico.reagendar(this.agendamento, medicoSelecionado, paciente, dateTime)) {
                        JOptionPane.showMessageDialog(null, "Agendamento atualizado com sucesso.");
                        this.dispose();
                        Home view = new Home();
                        view.setVisible(true);
                    }
                }
            }
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Formato de data ou hora inválido", "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_botao_ag_agendarActionPerformed

