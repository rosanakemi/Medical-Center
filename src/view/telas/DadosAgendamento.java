    private void carregarAgendamento(Agendamento agendamento) {
        this.agendamento = agendamento;

        // Carregar Paciente
        this.paciente = this.agendamento.getPaciente();
        if (this.paciente != null) {
            campo_pac_nome.setText(this.paciente.getNome());
        }

        // Carregar Medico
        Medico medico = this.agendamento.getMedico();
        if (medico != null) {
            combo_medico.setSelectedItem(medico);
            campo_med_especialidade.setText(medico.getEspecialidades());
        }

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        campo_ag_data.setText(dateFormat.format(this.agendamento.getDataHora().getTime()));

        SimpleDateFormat timeFormat = new SimpleDateFormat("HH:mm");
        campo_ag_horario.setText(timeFormat.format(this.agendamento.getDataHora().getTime()));
        
        botao_ag_agendar.setText("Atualizar");
    }

    private void carregarMedicosCombo() {
        MedicoServico servico = new MedicoServico();
        ArrayList<Medico> lista_medicos = servico.consultarTodos();

        if (lista_medicos != null) {
            for (Medico medico : lista_medicos) {
                combo_medico.addItem(medico);
            }

            Medico medicoIndexZero = (Medico) combo_medico.getItemAt(0);

            if (medicoIndexZero != null) {
                campo_med_especialidade.setText(medicoIndexZero.getEspecialidades());
            }

            combo_medico.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    campo_med_especialidade.setText("");
                    Medico medicoSelecionado = (Medico) combo_medico.getSelectedItem();
                    if (medicoSelecionado != null) {
                        campo_med_especialidade.setText(medicoSelecionado.getEspecialidades());
                    }
                }
            });
        }
    }

