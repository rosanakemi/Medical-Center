    private void botao_pac_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_pac_consultarActionPerformed
        if (campo_pac_cpf.getText().isEmpty()) {
            // Exibe uma mensagem de alerta informando que o campo de CPF do paciente está vazio
            JOptionPane.showMessageDialog(null, "Campo nome vazio");
            // Define o foco no campo para preencher
            campo_pac_cpf.requestFocus();
            return; // Sai do método, pois a validação falhou
        }

        PacienteServico servico = new PacienteServico();
        Paciente paciente = servico.consultarPorCPF(campo_pac_cpf.getText());

        campo_pac_nome.setText("");
        if (paciente != null) {
            this.paciente = paciente;
            campo_pac_nome.setText(paciente.getNome());
        } else {
            JOptionPane.showMessageDialog(null, "Paciente não encontrado.");
            // Define o foco no campo para preencher
            campo_pac_cpf.requestFocus();
        }
    }//GEN-LAST:event_botao_pac_consultarActionPerformed

